package edu.csxy.order.service_domain;

public class Permission_Bean {
	private int C_permission_id;
	private String C_permission_name;
	private String C_permission_picture;
	private String C_permission_date;
	public int getC_permission_id() {
		return C_permission_id;
	}
	public void setC_permission_id(int c_permission_id) {
		C_permission_id = c_permission_id;
	}
	public String getC_permission_name() {
		return C_permission_name;
	}
	public void setC_permission_name(String c_permission_name) {
		C_permission_name = c_permission_name;
	}
	public String getC_permission_picture() {
		return C_permission_picture;
	}
	public void setC_permission_picture(String c_permission_picture) {
		C_permission_picture = c_permission_picture;
	}
	public String getC_permission_date() {
		return C_permission_date;
	}
	public void setC_permission_date(String c_permission_date) {
		C_permission_date = c_permission_date;
	}
	
}
